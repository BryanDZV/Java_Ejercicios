package mapas;

public class Pareja {
	String palEs;
	String palIng;
	
	public Pareja(String palEs, String palIng) {
		super();
		this.palEs = palEs;
		this.palIng = palIng;
	}
	
	public String getPalEs() {
		return palEs;
	}
	public void setPalEs(String palEs) {
		this.palEs = palEs;
	}
	public String getPalIng() {
		return palIng;
	}
	public void setPalIng(String palIng) {
		this.palIng = palIng;
	}

}
